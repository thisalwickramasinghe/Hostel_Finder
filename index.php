  <script>
    function toggleDropdown() {
      var dropdownContent = document.getElementById("dropdown-content");
      if (dropdownContent.style.display === "block") {
        dropdownContent.style.display = "none";
      } else {
        dropdownContent.style.display = "block";
      }
    }
    
    function changeColor(userType, hover) {
      var wardenLink = document.getElementById("wardenLink");
      var hostelOwnerLink = document.getElementById("hostelOwnerLink");
    
      if (hover) {
        if (userType === 'warden') {
          wardenLink.style.backgroundColor = '#2196f3'; // Change background color to blue for warden on hover
        } else if (userType === 'hostelOwner') {
          hostelOwnerLink.style.backgroundColor = '#2196f3'; // Change background color to blue for hostel owner on hover
        }
      } else {
        if (userType === 'warden') {
          wardenLink.style.backgroundColor = ''; // Reset background color for warden when not hovering
        } else if (userType === 'hostelOwner') {
          hostelOwnerLink.style.backgroundColor = ''; // Reset background color for hostel owner when not hovering
        }
      }
    }
    </script>

